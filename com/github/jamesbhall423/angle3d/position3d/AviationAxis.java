package com.github.jamesbhall423.angle3d.position3d;

/**
 * Represents the three primary axes in aviation:
 * <ul>
 *   <li><b>LONGITUDINAL</b> - The axis pointing forward (nose to tail).</li>
 *   <li><b>LATERAL</b> - The axis pointing to the right (wing to wing).</li>
 *   <li><b>VERTICAL</b> - The axis pointing upward (bottom to top of the aircraft).</li>
 * </ul>
 * These axes are used to define the movement and orientation of an aircraft in three-dimensional space.
 * 
 * @author James Hall
 */
public enum AviationAxis {
    LONGITUDINAL, LATERAL, VERTICAL
}